_FW_PIPELINE = """\
format: fixed_width
dest_table: transactions
connector:
  type: sqlite
  url: {dest}
columns:
  - source: account_number
    dest: account_number
    type: string
    required: true
    start: 1
    width: 10
  - source: transaction_date
    dest: transaction_date
    type: date
    required: true
    start: 11
    width: 8
  - source: amount
    dest: amount_cents
    type: integer
    required: true
    start: 19
    width: 12
"""


def test_fixed_width_inspect_is_rejected(tmp_path):
    """inspect does not support fixed_width and says so (guide's CLI workflow)."""
    data = _write(tmp_path / "txns.fwf", "ACCT00000120240115000000150000\n")
    result = CliRunner().invoke(cli, ["inspect", data, "--format", "fixed_width"])

    assert result.exit_code != 0
    assert "does not support fixed_width" in result.output


def test_fixed_width_run_coerces_compact_date(tmp_path):
    """A YYYYMMDD field under a date column ingests and stores ISO YYYY-MM-DD."""
    landing = tmp_path / "landing"
    landing.mkdir()
    _write(landing / "txns.fwf", "ACCT00000120240115000000150000\n")
    config = _write(
        tmp_path / "pipeline.yaml",
        _FW_PIPELINE.format(dest=_sqlite(tmp_path, "fw.db")),
    )
    audit = _sqlite(tmp_path, "fw-audit.db")

    result = CliRunner().invoke(
        cli, ["run", "--dir", str(landing), "--config", config,
              "--audit-db-url", audit, "--no-progress"]
    )

    assert result.exit_code == 0
    assert "Committed: 1" in result.output

    import sqlite3
    row = sqlite3.connect(f"{tmp_path}/fw.db").execute(
        "select transaction_date, amount_cents from transactions"
    ).fetchone()
    assert row == ("2024-01-15", 150000)


# --- docs/guides/quarantine.md (the "Try the full loop" walkthrough) ------------

_QUARANTINE_PIPELINE = """\
format: ndjson
dest_table: partner_facts
connector:
  type: sqlite
  url: {dest}
quarantine:
  enabled: true
  dir: {qdir}
  max_invalid_fraction: 0.5
  max_invalid_rows: 100
columns:
  - source: filed
    dest: filed
    type: string
    required: true
  - source: val
    dest: value
    type: float
    required: true
"""

_PARTNER_NDJSON = (
    '{"filed": "2024-11-01", "val": 391035000000}\n'
    '{"filed": "2024-11-01", "val": "n/a"}\n'
    '{"filed": "2024-08-01", "val": 85777000000}\n'
    '{"filed": "2024-05-01", "val": ""}\n'
    '{"filed": "2024-02-01", "val": 119575000000}\n'
)


def test_quarantine_full_loop(tmp_path):
    """partial commit -> status -> audit export badge -> re-drop, as the
    quarantine guide's walkthrough shows (guards #252 and #253)."""
    landing = tmp_path / "qland"
    landing.mkdir()
    _write(landing / "partner-facts.ndjson", _PARTNER_NDJSON)
    qdir = tmp_path / "quarantine"
    config = _write(
        tmp_path / "pipeline.yaml",
        _QUARANTINE_PIPELINE.format(dest=_sqlite(tmp_path, "dest.db"), qdir=qdir),
    )
    audit = _sqlite(tmp_path, "audit.db")
    runner = CliRunner()

    # Run: 3 good rows commit, 2 bad rows quarantine (under the 50% threshold).
    run = runner.invoke(
        cli, ["run", "--dir", str(landing), "--config", config,
              "--audit-db-url", audit, "--no-progress"]
    )
    assert run.exit_code == 0
    assert "Quarantined rows: 2" in run.output

    # status surfaces the quarantined total.
    status = runner.invoke(cli, ["status", "--audit-db-url", audit])
    assert "QUARANTINED ROWS: 2" in status.output

    # Audit Export badges the partial commit and shows the sidecar path (#252).
    site = tmp_path / "site" / "index.html"
    runner.invoke(cli, ["export-audit", "--audit-db-url", audit, "--output", str(site)])
    html = site.read_text()
    assert "quarantined" in html
    assert ".quarantine.ndjson" in html

    # re-drop unwraps the sidecar into a clean NDJSON File (#253).
    sidecar = next(qdir.glob("*.quarantine.ndjson"))
    redrop = runner.invoke(cli, ["redrop-quarantine", "--sidecar", str(sidecar)])
    assert redrop.exit_code == 0
    assert "Wrote 2 row(s)" in redrop.output
