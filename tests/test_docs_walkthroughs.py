"""Executable guard for the client-facing guides.

Each test runs a documented walkthrough end-to-end through the real `filedge`
CLI and asserts on the *stable* parts of the output that the corresponding guide
shows — exit codes, key substrings, structural JSON keys — not exact bytes
(timestamps, UUIDs, durations, and latencies vary run to run).

The point is to make the guides un-driftable: if a code change alters an output
a guide depicts (a renamed flag, a removed summary field, a changed inference
tier, a rebranded header), the matching test here fails and prompts a doc
update. When you edit one of these guides' commands or output, update the
mirroring test in the same change.

Mirrors:
  docs/getting-started.md, docs/guides/inspect.md, docs/guides/validate.md,
  docs/guides/run.md, docs/guides/preview.md, docs/guides/healthcheck.md,
  docs/guides/compact.md, docs/guides/audit-export.md,
  docs/guides/fixed-width.md, docs/guides/quarantine.md
"""

import json

import pytest
from click.testing import CliRunner

from filedge.cli import cli


def _write(path, body):
    path.write_text(body)
    return str(path)


def _sqlite(tmp_path, name):
    return f"sqlite:///{tmp_path}/{name}"


_ORDERS_PIPELINE = """\
format: csv
dest_table: orders
write_mode: append
connector:
  type: sqlite
  url: {dest}
columns:
  - source: order_id
    dest: order_id
    type: string
    required: true
  - source: amount
    dest: amount
    type: float
    required: true
"""

_CLEAN_CSV = "order_id,amount\nORD-1,9.99\nORD-2,14.50\nORD-3,0.01\n"


def _orders_workspace(tmp_path):
    incoming = tmp_path / "incoming"
    incoming.mkdir()
    _write(incoming / "data.csv", _CLEAN_CSV)
    config = _write(
        tmp_path / "pipeline.yaml",
        _ORDERS_PIPELINE.format(dest=_sqlite(tmp_path, "orders.db")),
    )
    return str(incoming), config


# --- docs/getting-started.md ---------------------------------------------------

def test_getting_started_happy_path(tmp_path):
    """inspect -> validate -> run -> status, as Steps 1-4 show."""
    data = _write(tmp_path / "data.csv", _CLEAN_CSV)
    incoming, config = _orders_workspace(tmp_path)
    audit = _sqlite(tmp_path, "filedge.db")
    runner = CliRunner()

    # Step 1: inspect emits a filedge-branded YAML header (guards the #258 branding fix).
    insp = runner.invoke(cli, ["inspect", data])
    assert insp.exit_code == 0
    assert "# Generated by filedge inspect" in insp.output
    assert "columns:" in insp.output

    # Step 3: validate is clean and exits 0.
    val = runner.invoke(cli, ["validate", data, "--config", config])
    assert val.exit_code == 0
    assert "rows checked, no failures" in val.output

    # Step 4: run commits exactly one File.
    run = runner.invoke(
        cli, ["run", "--dir", incoming, "--config", config,
              "--audit-db-url", audit, "--no-progress"]
    )
    assert run.exit_code == 0
    assert "Committed: 1" in run.output

    # status reports it COMMITTED.
    status = runner.invoke(cli, ["status", "--audit-db-url", audit])
    assert status.exit_code == 0
    assert "COMMITTED:  1" in status.output


def test_getting_started_validate_failure(tmp_path):
    """The 'or with failures' block: bad rows -> exit 1 and a failure count."""
    config = _write(
        tmp_path / "pipeline.yaml",
        _ORDERS_PIPELINE.format(dest=_sqlite(tmp_path, "orders.db")),
    )
    bad = _write(tmp_path / "bad.csv", "order_id,amount\nORD-1,9.99\nORD-2,n/a\n")

    result = CliRunner().invoke(cli, ["validate", bad, "--config", config])

    assert result.exit_code == 1
    assert "failure(s) in" in result.output


# --- docs/guides/inspect.md -----------------------------------------------------

def test_inspect_confidence_tiers(tmp_path):
    """Confidence tiers as the guide depicts: clean text high, nulls low,
    mixed numeric/text ambiguous (guards #258)."""
    data = _write(
        tmp_path / "data.csv",
        "name,amount,code\n"
        "Alice,9.99,A\n"
        "Bob,,B\n"          # amount null -> low
        "Carol,3.50,7\n",   # code mixes text + numeric -> ambiguous
    )
    result = CliRunner().invoke(cli, ["inspect", data])

    assert result.exit_code == 0
    assert "name" in result.output and "string, high" in result.output
    assert "amount" in result.output and "float, low" in result.output
    assert "code" in result.output and "ambiguous" in result.output


# --- docs/guides/validate.md ----------------------------------------------------

def test_validate_json_shape(tmp_path):
    config = _write(
        tmp_path / "pipeline.yaml",
        _ORDERS_PIPELINE.format(dest=_sqlite(tmp_path, "orders.db")),
    )
    data = _write(tmp_path / "data.csv", _CLEAN_CSV)

    result = CliRunner().invoke(cli, ["validate", data, "--config", config, "--json"])

    assert result.exit_code == 0
    # The JSON line is the last line of stdout.
    payload = json.loads(result.output.strip().splitlines()[-1])
    assert set(payload) >= {"rows_checked", "failures", "undeclared_columns"}
    assert payload["rows_checked"] == 3
    assert payload["failures"] == []


# --- docs/guides/run.md ---------------------------------------------------------

def test_run_json_summary_fields(tmp_path):
    """The --json summary carries the fields run.md documents, incl.
    quarantined_rows (guards the field run.md was missing)."""
    incoming, config = _orders_workspace(tmp_path)
    audit = _sqlite(tmp_path, "filedge.db")

    result = CliRunner().invoke(
        cli, ["run", "--dir", incoming, "--config", config,
              "--audit-db-url", audit, "--json"]
    )

    assert result.exit_code == 0
    summary = json.loads(result.output.strip().splitlines()[-1])
    assert set(summary) >= {
        "run_id", "files_scanned", "new_files", "committed", "failed",
        "skipped", "reclaimed", "retried", "rows_committed",
        "quarantined_rows", "bytes_processed",
    }
    assert summary["committed"] == 1


# --- docs/guides/preview.md -----------------------------------------------------

def test_preview_renders_ascii_table(tmp_path):
    """preview renders an ASCII table (| + -), not box-drawing characters."""
    data = _write(
        tmp_path / "data.csv",
        "name,amount,order_date\nAlice,9.99,2024-01-15\nBob,14.50,2024-01-16\n",
    )
    result = CliRunner().invoke(cli, ["preview", data, "--rows", "2"])

    assert result.exit_code == 0
    assert "|" in result.output and "+" in result.output
    # No box-drawing characters leaked back in.
    for ch in ("│", "┼", "─"):
        assert ch not in result.output


# --- docs/guides/healthcheck.md -------------------------------------------------

def test_healthcheck_text_and_json(tmp_path):
    config = _write(
        tmp_path / "pipeline.yaml",
        _ORDERS_PIPELINE.format(dest=_sqlite(tmp_path, "orders.db")),
    )
    audit = _sqlite(tmp_path, "filedge.db")
    runner = CliRunner()

    text = runner.invoke(cli, ["healthcheck", "--config", config, "--audit-db-url", audit])
    assert text.exit_code == 0
    assert "audit_db: ok" in text.output
    assert "destination: ok" in text.output

    js = runner.invoke(
        cli, ["healthcheck", "--config", config, "--audit-db-url", audit, "--json"]
    )
    assert js.exit_code == 0
    payload = json.loads(js.output.strip().splitlines()[-1])
    assert payload["healthy"] is True
    assert {c["name"] for c in payload["checks"]} == {"audit_db", "destination"}


