# --- docs/guides/compact.md -----------------------------------------------------

def test_compact_merges_ndjson(tmp_path):
    incoming = tmp_path / "incoming"
    incoming.mkdir()
    _write(incoming / "a.ndjson", '{"id":"1"}\n{"id":"2"}\n')
    _write(incoming / "b.ndjson", '{"id":"3"}\n')
    out = tmp_path / "compacted"

    result = CliRunner().invoke(
        cli, ["compact", "--watched-dir", str(incoming), "--output", str(out)]
    )

    assert result.exit_code == 0
    assert "Files compacted: 2" in result.output
    merged = list(out.glob("*.ndjson"))
    assert len(merged) == 1
    assert len(merged[0].read_text().strip().splitlines()) == 3


